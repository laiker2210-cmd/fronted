"use strict";

let questions = [
    "Столица России?",
    "Сколько будет 2 + 2?",
    "Какой газ преобладает в атмосфере Земли?",
    "Кто написал 'Войну и мир'?",
    "Сколько планет в Солнечной системе?",
    "В каком году Гагарин полетел в космос?",
    "Самая длинная река мира?"
];

let correctAnswers = ["A", "B", "A", "C", "C", "A", "B"];

let variants = [
    "A:Москва\nB:Санкт-Петербург\nC:Казань\nD:Новосибирск",
    "A:3\nB:4\nC:5\nD:6",
    "A:Кислород\nB:Азот\nC:Углекислый газ\nD:Водород",
    "A:Достоевский\nB:Чехов\nC:Толстой\nD:Пушкин",
    "A:7\nB:9\nC:8\nD:10",
    "A:1961\nB:1957\nC:1965\nD:1970",
    "A:Миссисипи\nB:Нил\nC:Амазонка\nD:Янцзы"
];

let money = [1000, 2000, 5000, 10000, 20000, 50000, 1000000];

let save = [2, 5];
let final = 0;
let Sum = 0;

alert("Добро пожаловать в игру 'Первый миллион'!\n\n" +
    "Вам предстоит ответить на 7 вопросов.\n" +
    "Несгораемые суммы на вопросах 3 и 6.\n\n" +
    "Удачи!");

for (let i = 0; i < questions.length; i++) {

    let answer = prompt(
        "Вопрос " + (i + 1) + " из " + questions.length + "\n\n" +
        questions[i] + "\n\n" + variants[i] + "\n\n" +
        "Введите букву ответа (A, B, C или D):"
    );


    final = i + 1;

    if (answer === correctAnswers[i]) {
        Sum = money[i];
        alert("Правильно! \n\n" +
            "Вы выиграли " + Sum + " денежных единиц.");

        if (save.indexOf(i) !== -1) {
            alert("Поздравляем! Вы дошли до несгораемой суммы " +
                Sum + "!");
        }

        if (i === questions.length - 1) {
            alert("Поздравляю! \n\n" +
                "Вы ответили на ВСЕ вопросы!\n" +
                "Вы выиграли миллион!");
            break;
        }

    } else {

        alert("Неправильно!\n\n" +
            "Правильный ответ был: " + correctAnswers[i] + "\n" +
            "Конец игры!");

        if (Sum === 0) {
            alert("Вы ничего не выиграли.\n" +
                "Попробуйте ещё раз!");
        } else {
            let safeSum = 0;
            for (let j = save.length - 1; j >= 0; j--) {
                if (save[j] < i) {
                    safeSum = money[save[j]];
                    break;
                }
            }

            alert("Вы выиграли: " + safeSum + " денежных единиц.\n" +
                "(Ваша несгораемая сумма)");
        }

        break;
    }
}

alert("ИГРА ОКОНЧЕНА\n\n" +
    "Вы дошли до вопроса: " + final + "\n" +
    "Ваш результат: " + Sum + " денежных единиц\n\n" +
    "Спасибо за игру!");